const moviesGrid = document.getElementById('moviesGrid');
const modal = document.getElementById('movieModal');
const favorites = JSON.parse(localStorage.getItem('favorites') || '[]');

const getMovieTitle = (movie) => {
    return movie.nameEn || movie.nameRu;
};

const displayFavorites = () => {
    const favoriteMovies = app.items.filter(movie =>
        favorites.includes(movie.kinopoiskId)
    );

    if (favoriteMovies.length === 0) {
        moviesGrid.innerHTML = '<p class="no-favorites">У вас пока нет избранных фильмов</p>';
        return;
    }

    favoriteMovies.forEach(movie => {
        const movieCard = document.createElement('div');
        movieCard.className = 'movie-card';
        movieCard.innerHTML = `
            <img src="${movie.posterUrl}" alt="${getMovieTitle(movie)}">
            <div class="movie-title">${getMovieTitle(movie)}</div>
        `;
        movieCard.addEventListener('click', () => showMovieDetails(movie));
        moviesGrid.appendChild(movieCard);
    });
};

const isMovieFavorite = (movieId) => {
    return favorites.includes(movieId);
};

const showMovieDetails = (movie) => {
    const isFavorite = isMovieFavorite(movie.kinopoiskId);
    const modalContent = `
        <div class="modal-body">
            <div class="movie-poster">
                <img src="${movie.posterUrl}" alt="${getMovieTitle(movie)}">
                <button class="favorite-btn" data-id="${movie.kinopoiskId}">
                    ${isFavorite ? '❤️' : '🤍'}
                </button>
            </div>
            <div class="movie-info">
                <h2>${getMovieTitle(movie)}</h2>
                <div class="info-item">Год: ${movie.year}</div>
                <div class="info-item">Страны: ${movie.countries.map(c => c.country).join(', ')}</div>
                <div class="info-item">Жанры: ${movie.genres.map(g => g.genre).join(', ')}</div>
                <div class="info-item">Длительность: ${movie.duration} мин</div>
                <div class="info-item">Премьера в России: ${movie.premiereRu}</div>
            </div>
        </div>
    `;

    modal.querySelector('.modal-body').innerHTML = modalContent;
    modal.style.display = 'block';

    const favoriteBtn = modal.querySelector('.favorite-btn');
    favoriteBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        toggleFavorite(movie.kinopoiskId, favoriteBtn);
        moviesGrid.innerHTML = '';
        displayFavorites();
    });
};

const toggleFavorite = (movieId, btn) => {
    const index = favorites.indexOf(movieId);

    if (index === -1) {
        favorites.push(movieId);
        btn.textContent = '❤️';
    } else {
        favorites.splice(index, 1);
        btn.textContent = '🤍';
        modal.style.display = 'none';
    }

    localStorage.setItem('favorites', JSON.stringify(favorites));
};

const closeBtn = modal.querySelector('.close');
closeBtn.addEventListener('click', () => {
    modal.style.display = 'none';
});

window.addEventListener('click', (e) => {
    if (e.target === modal) {
        modal.style.display = 'none';
    }
});

displayFavorites();